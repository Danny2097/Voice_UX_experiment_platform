#!/usr/bin/env bash
# ──────────────────────────────────────────────────────────────────
#  generate-env.sh
#  Generates a blank .env file with all required keys.
#  Run once during first-time setup:  bash generate-env.sh
# ──────────────────────────────────────────────────────────────────

set -euo pipefail

TARGET=".env"

if [ -f "$TARGET" ]; then
  echo "⚠️  $TARGET already exists."
  read -r -p "   Overwrite? [y/N] " confirm
  case "$confirm" in
    [yY]) echo "   Overwriting..." ;;
    *)    echo "   Aborted. Existing $TARGET was not modified."; exit 0 ;;
  esac
fi

cat > "$TARGET" << 'EOF'
# ══════════════════════════════════════════════════════════════
#  Voice Control Research Platform — Environment Configuration
#  Generated by generate-env.sh — fill in all values below.
# ══════════════════════════════════════════════════════════════

# ── Researcher login (protects Experiment Manager when live) ──
ADMIN_USER=
ADMIN_PASSWORD=

# ── PostgreSQL credentials ────────────────────────────────────
DB_USER=
DB_PASS=

# ── Host port (default 8080) ──────────────────────────────────
VRP_PORT=
EOF

echo "✅  $TARGET created. Open it and fill in all values before running docker compose up."
